/* Create new tables */
CREATE TABLE EMPLOYEE (
	EMPLOYEE_NUMBER    	NUMBER(6) PRIMARY KEY
);

CREATE TABLE WAREHOUSE (
	WAREHOUSE_ID     	VARCHAR(4) PRIMARY KEY
);

CREATE TABLE BIN (
	BIN_NUMBER			NUMBER,
	WAREHOUSE_ID    	VARCHAR(4) NOT NULL REFERENCES WAREHOUSE(WAREHOUSE_ID),
	BIN_CAPACITY    	NUMBER NOT NULL,
	CONSTRAINT PK_BIN 	PRIMARY KEY(BIN_NUMBER, WAREHOUSE_ID)
);

CREATE TABLE PART (
	PART_NUMBER			VARCHAR(5) PRIMARY KEY
);

CREATE TABLE SUBPART (
	PART_NUMBER			VARCHAR(5) NOT NULL REFERENCES PART(PART_NUMBER),
	ASSEMBLY_NUMBER		VARCHAR(5) NOT NULL,
	CONSTRAINT PK_SUBPART PRIMARY KEY(PART_NUMBER, ASSEMBLY_NUMBER)
);

CREATE TABLE BATCH (
    BATCH_NUMBER		NUMBER PRIMARY KEY,
    PART_NUMBER			VARCHAR(5) NOT NULL,
    WAREHOUSE_ID		VARCHAR(4),    
    BIN_NUMBER			NUMBER NOT NULL,
    BATCH_SIZE      	NUMBER,
    DATE_IN         	DATE,
    EMPLOYEE_NUMBER     NUMBER(6) NOT NULL,
    CONSTRAINT FK_BATCH_ITEM_PART_NUMBER FOREIGN KEY (PART_NUMBER) REFERENCES PART(PART_NUMBER),
    CONSTRAINT FK_BATCH_ITEM_BATCH_NUMBER FOREIGN KEY (BATCH_NUMBER) REFERENCES BATCH(BATCH_NUMBER),
    CONSTRAINT FK_BATCH_ITEM_EMPLOYEE_NUMBER FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLOYEE(EMPLOYEE_NUMBER)
);

CREATE TABLE ITEM (
    ITEM_NUMBER         NUMBER,
    PART_NUMBER			VARCHAR(5) NOT NULL REFERENCES PART(PART_NUMBER),
    BATCH_NUMBER    	NUMBER NOT NULL REFERENCES BATCH(BATCH_NUMBER),
    DATE_OUT         	DATE,
    CHECKED_OUT        	DATE,
    EMPLOYEE_NUMBER    	NUMBER NOT NULL,
    CONSTRAINT PK_ITEM 	PRIMARY KEY(ITEM_NUMBER, BATCH_NUMBER, PART_NUMBER),    
    CONSTRAINT FK_ITEM_EMPLOYEE_NUMBER FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLOYEE(EMPLOYEE_NUMBER)
);

CREATE TABLE CURRENT_BACKORDER (
    PART_NUMBER			VARCHAR(5) NOT NULL,
    ORIGINAL_QTY    	NUMBER,
    REMAINING_QTY     	NUMBER,
    BO_DATE        		DATE,
    EMPLOYEE_NUMBER    	NUMBER NOT NULL,
    CONSTRAINT FK_CB_PART_NUMBER FOREIGN KEY (PART_NUMBER) REFERENCES PART(PART_NUMBER),
    CONSTRAINT FK_CB_EMPLOYEE_NUMBER FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLOYEE(EMPLOYEE_NUMBER)
);

CREATE TABLE MANAGER (
    EMPLOYEE_NUMBER		NUMBER(6) NOT NULL PRIMARY KEY,
    CONSTRAINT FK_MANAGER FOREIGN KEY(EMPLOYEE_NUMBER) REFERENCES EMPLOYEE(EMPLOYEE_NUMBER)
);
    
CREATE TABLE WORKER (
    EMPLOYEE_NUMBER		NUMBER(6) NOT NULL PRIMARY KEY,
	MANAGER				NUMBER(6) NOT NULL REFERENCES MANAGER(EMPLOYEE_NUMBER), 
    CONSTRAINT FK_WORKER FOREIGN KEY(EMPLOYEE_NUMBER) REFERENCES EMPLOYEE(EMPLOYEE_NUMBER)
);

CREATE TABLE OLD_BACKORDER (
    PART_NUMBER     	VARCHAR(5) NOT NULL,
    BO_DATE         	DATE,
    FULFILLED_DATE  	DATE,
    ORIGINAL_QTY    	NUMBER,
    EMPLOYEE_NUMBER 	NUMBER NOT NULL,
    CONSTRAINT PK_OLD_BACKORDER PRIMARY KEY (PART_NUMBER, BO_DATE, FULFILLED_DATE),
    CONSTRAINT FK_OLD_BACKORDER FOREIGN KEY(EMPLOYEE_NUMBER) REFERENCES MANAGER(EMPLOYEE_NUMBER),
    CONSTRAINT FK_OLD_BACKORDER2 FOREIGN KEY(PART_NUMBER) REFERENCES PART(PART_NUMBER)
);

CREATE TABLE EMP_NAME (
	FIRST_NAME			VARCHAR(10)	NOT NULL,
	MIDDLE_NAME     	VARCHAR(10),	
	LAST_NAME			VARCHAR(20)	NOT NULL,
	EMPLOYEE_NUMBER		NUMBER(6)	NOT NULL,
	CONSTRAINT PK_ENAME PRIMARY KEY (FIRST_NAME, LAST_NAME),
	CONSTRAINT FK_ENAME_EMPLOYEE_NUMBER FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLOYEE (EMPLOYEE_NUMBER)
);

CREATE TABLE EMP_PHONE (
	EMP_PHONE_AREA		VARCHAR(3)	NOT NULL,
	EMP_PHONE_NUMBER	VARCHAR(8)	NOT NULL,
	EMPLOYEE_NUMBER		NUMBER(6)	NOT NULL,
	CONSTRAINT PK_EMP_PHONE PRIMARY KEY (EMP_PHONE_AREA, EMP_PHONE_NUMBER),
	CONSTRAINT FK_EMP_PHONE_EMPLOYEE_NUMBER FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLOYEE(EMPLOYEE_NUMBER)
); 

CREATE TABLE EMP_ADDRESS (
	EMPLOYEE_NUMBER		NUMBER(6)	NOT NULL,
	EMP_ADDRESS_STNUM	NUMBER(5)	NOT NULL,
	EMP_ADDRESS_STNAME	VARCHAR(20)	NOT NULL,
	EMP_ADDRESS_CITY	VARCHAR(20)	NOT NULL,
	EMP_ADDRESS_STATE	VARCHAR(2)	NOT NULL,
	EMP_ADDRESS_ZIP		NUMBER(5)	NOT NULL, 
	CONSTRAINT PK_EMP_ADDRESS PRIMARY KEY (EMP_ADDRESS_STNUM, EMP_ADDRESS_STNAME, EMP_ADDRESS_CITY, EMP_ADDRESS_STATE, EMP_ADDRESS_ZIP),
	CONSTRAINT FK_EMP_ADDRESS_EMPLOYEE_NUMBER FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLOYEE(EMPLOYEE_NUMBER)
);

